SELECT p.name, pc.person_role, 
       f.name, f.country, 
       f.fees, date_part('year', f.release_year)
FROM person2content pc
   JOIN films f
    ON pc.content_id = f.id
   JOIN persons p
    ON pc.person_id = p.id
ORDER BY p.name, f.name
